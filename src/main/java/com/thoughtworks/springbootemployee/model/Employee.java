package com.thoughtworks.springbootemployee.model;

public class Employee {
    private int id;
    private String name;
    private int age;
    private String gender;
    private int salary;

    public int getId() {
        return id;
    }

    public String getName() {
        return name;
    }

    public int getAge() {
        return age;
    }

    public String getGender() {
        return gender;
    }

    public int getSalary() {
        return salary;
    }
}
